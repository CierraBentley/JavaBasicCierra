package com.neotech.lesson02;

public class CommentDemoo {

	public static void main(String[] args) {

		System.out.println("What's up Yilmaz?");

		System.out.println("How you doing?");
		

		/*
		 * This is a multi-line comment
		 * I can add some extra explanation
		 * In multiple lines
		 */

		// println - prints and then moves to the new line
		// print - prints and does not move to the new line

		System.out.print("1");
		;
		System.out.print("2");
		System.out.print("3"); // after printing 3, it will stay at the same line

		System.out.println("4"); // after printing 4. it will move at the new line
		System.out.println("5");
		System.out.println("6");

		// To debug - double click the line you want to stop at
		// It will make a little dot next to number
		// Then click Debug in tool bar to run that
		// To stop it, click red square
		// Double click again to remove dot next to number

		// New line in middle of sentence below using - \n
		System.out.println("Do you have any questions? \nOr not?");
		System.out.println("Do you like Java? \nOr no?");

		
		//To format the code --> command + shift + F
		
		
		
		
		
	}

}
