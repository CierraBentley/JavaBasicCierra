package com.neotech.lesson02;

public class PrimitiveDataTypes {

	public static void main (String[] args) {
		

	byte cierra = 9;  // the range is -128 to 127 (256 dif values)
	
	short berkant = 145; // range is -32768 to 32767 
	
	int james = 234567; // range is -billion to 2 billion 
	
	long bankAccount = 98765874453674L; //if number is bigger than 2 billion
										//use long with L or l at end 
	
	float cierra1 = 23.45F; //float stores decimal numbers 
							// must use f or F at the end 
	
	double berkant2 = 23.45; //you can do decimals with float 
	
	
	char var3 = 'P'; //store a single character 
	char var4 = '%';
	char var5 = 'p'; 
	
	boolean jam = true; //to store boolean values (true or false)

	
	System.out.println(bankAccount);
	System.out.println(berkant);
	System.out.println(cierra);
	System.out.println(james);
	System.out.println(cierra1);
	System.out.println(berkant2);
	System.out.println(var3);
	System.out.println(var4);
	System.out.println(var5);
	System.out.println(jam);
	
	
		
		
		
		
		
	}
	
	
}
