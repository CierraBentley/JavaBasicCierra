package com.neotech.lesson03;

public class VariableValuesReviewHW {

	public static void main(String[] args) {

		/*
		 * Create a Java program and name it VariableValues 
		 * In your program create variables
		 * to store all different types of primitive data 
		 * print the value of each variable 
		 * update the value of each variable 
		 * print the value of each variable after updating
		 */

		//FORMULA:
		//Data type name (identifier) assignment operator (=) value ; 	
		
		//Assigning values
		byte a = 5;
		short b = 130;
		int c = 2500;
		long d = 121345675l;
		float e = 2.456789f;
		double f = 5.456789345;
		char g = 'g';
		boolean h = true;
		
		//Printing values 
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		
		//Reassigning variable values 
		a = 15;
		b = 170;
		c = 3000;
		d = 987654321l;
		e = 3.456f;
		f = 1.234;
		g = 'c';
		h = false;
		
	}

}
